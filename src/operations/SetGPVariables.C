        for (int i = 0; i < 100; ++i) {
            TT_vector[i] = start_TT + i * step_TT;
        }

        // Generate values for F_plot
        for (int i = 0; i < 100; ++i) {
            FF_vector[i] = start_FF + i * step_FF;
        }

        // Perform the interpolation
